class CBSGlobalData
{
	/**************************************************************************/
	
	static function setGlobalData($name,$functionCallback,$refresh=false)
	{
		global $cbsGlobalData;
		
		if(isset($cbsGlobalData[$name]) && (!$refresh)) return($cbsGlobalData[$name]);
		
		$cbsGlobalData[$name]=call_user_func($functionCallback);
		
		return($cbsGlobalData[$name]);
	}

	/**************************************************************************/
}

